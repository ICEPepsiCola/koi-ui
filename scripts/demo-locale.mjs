/** Longest Chinese phrases first to avoid partial replacements. */
export const DEMO_ZH_TO_EN = [...DEMO_ZH_TO_EN_SOURCE].sort(
  (a, b) => b[0].length - a[0].length || a[0].localeCompare(b[0]),
);

const HAS_CJK = /[\u4e00-\u9fff]/;

export function hasCjk(value) {
  return HAS_CJK.test(String(value ?? ''));
}

/** Apply Chinese → English phrase map (demos, defaults, leftover copy). */
export function toEnglishDemoText(value) {
  let out = String(value ?? '');
  if (!hasCjk(out)) return out;
  for (const [zh, en] of DEMO_ZH_TO_EN) {
    if (out.includes(zh)) out = out.split(zh).join(en);
  }
  return out;
}

export function localizeDemoCode(code, lang) {
  if (lang !== 'en') return code;
  return toEnglishDemoText(code);
}
